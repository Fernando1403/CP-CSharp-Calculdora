namespace AppMethods
{
    public class Util
    {
        /// <summary>
        /// 06.03.2026 - fernando.gonzales - method to sum numbers
        /// </summary>
        /// <param name="num1">parametro que recebe o primeiro valor</param>
        /// <param name="num2">parametro que recebe o segundo valor</param>
        /// <returns>Retorna a soma dos valores</returns>
        public int Somar(int num1, int num2)
        {
            return num1 + num2;
        }

        /// <summary>
        /// 06.03.2026 - fernando.gonzales - method to sum 3 numbers
        /// </summary>
        /// <param name="num1">parametro que recebe o primeiro valor</param>
        /// <param name="num2">parametro que recebe o segundo valor</param>
        /// <param name="num3">parametro que recebe o terceiro valor</param>
        /// <returns></returns>
        public int Somar(int num1, int num2, int num3)
        {
            return num1 + num2 + num3;
        }

        /// <summary>
        /// 06.03.2026 - fernando.gonzales - method to sum 4 numbers
        /// </summary>
        /// <param name="num1">parametro que recebe o primeiro valor</param>
        /// <param name="num2">parametro que recebe o segundo valor</param>
        /// <param name="num3">parametro que recebe o terceiro valor</param>
        /// <param name="num4">parametro que recebe o quarto valor</param>
        /// <returns></returns>
        public int Somar(int num1, int num2, int num3, int num4)
        {
            return num1 + num2 + num3 + num4;
        }
    }
}
